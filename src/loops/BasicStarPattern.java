package loops;

public class BasicStarPattern {
	
	
//	*****
//	*****
//	*****
//	*****
//	*****
	
	public static void main(String[] args) {
		
		for(int j=1; j<=5; j++)// outer for loop
		{
			
		for(int i = 1; i<=5; i++)// inner for loop
		{
		System.out.print("*");
		
		}
		
		System.out.println();
		
		
		}
	}

}
