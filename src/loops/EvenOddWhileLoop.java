package loops;

public class EvenOddWhileLoop {
	
	
	public static void main(String[] args) {
		int i = 1;
		
		
		while(i<=20)
		{
			
			if(i%2==0)
			{
				System.out.println("Even number is "+i);
			}
			
			i = i + 1;
			
		}
		
//		WAP to print odd number
//		WAP to print the table of any number and that number should be taken by user as an argument
		
	}

}
