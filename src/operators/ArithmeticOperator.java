package operators;

public class ArithmeticOperator {
	
	public static void main(String[] args) {
		
		int i = 10;
		int j = 20;
		
//		addition '+'
		
		int k = i+j +40 ;
		
		System.out.println(k);
		
//		substraction '-'
		
		int h= i-j ;
		System.out.println(h);
		
//		multiplication '*'
		
		int l = i*j;
	
	
	System.out.println(l);
	
//	division '/'
	
	
	double m = i/j;
	
	System.out.println(m);
	
	double d = 10;
	
	double e = 20;
	
	double f = d/e;
	
	System.out.println(f);//0
	
//	modulus operator '%'
	
	int o = 10;
	int p = 3;
	
	int s = o%p;
	
	System.out.println(s);
	
	
		
		
	}

}
